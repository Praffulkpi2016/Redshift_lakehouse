TRUNCATE table bronze_bec_ods_stg.FA_MC_DEPRN_PERIODS;
INSERT INTO bronze_bec_ods_stg.FA_MC_DEPRN_PERIODS (
  SET_OF_BOOKS_ID,
  BOOK_TYPE_CODE,
  PERIOD_NAME,
  PERIOD_COUNTER,
  FISCAL_YEAR,
  PERIOD_NUM,
  PERIOD_OPEN_DATE,
  PERIOD_CLOSE_DATE,
  DEPRECIATION_BATCH_ID,
  RETIREMENT_BATCH_ID,
  RECLASS_BATCH_ID,
  TRANSFER_BATCH_ID,
  ADDITION_BATCH_ID,
  ADJUSTMENT_BATCH_ID,
  DEFERRED_DEPRN_BATCH_ID,
  CALENDAR_PERIOD_OPEN_DATE,
  CALENDAR_PERIOD_CLOSE_DATE,
  CIP_ADDITION_BATCH_ID,
  CIP_ADJUSTMENT_BATCH_ID,
  CIP_RECLASS_BATCH_ID,
  CIP_RETIREMENT_BATCH_ID,
  CIP_REVAL_BATCH_ID,
  CIP_TRANSFER_BATCH_ID,
  REVAL_BATCH_ID,
  DEPRN_ADJUSTMENT_BATCH_ID,
  DEPRN_RUN,
  KCA_OPERATION,
  kca_seq_id,
  kca_seq_date
)
(
  SELECT
    SET_OF_BOOKS_ID,
    BOOK_TYPE_CODE,
    PERIOD_NAME,
    PERIOD_COUNTER,
    FISCAL_YEAR,
    PERIOD_NUM,
    PERIOD_OPEN_DATE,
    PERIOD_CLOSE_DATE,
    DEPRECIATION_BATCH_ID,
    RETIREMENT_BATCH_ID,
    RECLASS_BATCH_ID,
    TRANSFER_BATCH_ID,
    ADDITION_BATCH_ID,
    ADJUSTMENT_BATCH_ID,
    DEFERRED_DEPRN_BATCH_ID,
    CALENDAR_PERIOD_OPEN_DATE,
    CALENDAR_PERIOD_CLOSE_DATE,
    CIP_ADDITION_BATCH_ID,
    CIP_ADJUSTMENT_BATCH_ID,
    CIP_RECLASS_BATCH_ID,
    CIP_RETIREMENT_BATCH_ID,
    CIP_REVAL_BATCH_ID,
    CIP_TRANSFER_BATCH_ID,
    REVAL_BATCH_ID,
    DEPRN_ADJUSTMENT_BATCH_ID,
    DEPRN_RUN,
    KCA_OPERATION,
    kca_seq_id,
    kca_seq_date
  FROM bec_raw_dl_ext.FA_MC_DEPRN_PERIODS
  WHERE
    kca_operation <> 'DELETE'
    AND COALESCE(kca_seq_id, '') <> ''
    AND (COALESCE(BOOK_TYPE_CODE, 'NA'), COALESCE(PERIOD_COUNTER, 0), COALESCE(SET_OF_BOOKS_ID, 0), kca_seq_id) IN (
      SELECT
        COALESCE(BOOK_TYPE_CODE, 'NA') AS BOOK_TYPE_CODE,
        COALESCE(PERIOD_COUNTER, 0) AS PERIOD_COUNTER,
        COALESCE(SET_OF_BOOKS_ID, 0) AS SET_OF_BOOKS_ID,
        MAX(kca_seq_id)
      FROM bec_raw_dl_ext.FA_MC_DEPRN_PERIODS
      WHERE
        kca_operation <> 'DELETE' AND COALESCE(kca_seq_id, '') <> ''
      GROUP BY
        COALESCE(BOOK_TYPE_CODE, 'NA'),
        COALESCE(PERIOD_COUNTER, 0),
        COALESCE(SET_OF_BOOKS_ID, 0)
    )
    AND kca_seq_date > (
      SELECT
        (
          executebegints - prune_days
        )
      FROM bec_etl_ctrl.batch_ods_info
      WHERE
        ods_table_name = 'fa_mc_deprn_periods'
    )
);