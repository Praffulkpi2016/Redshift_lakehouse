  REQUEST_ID,
  PROGRAM_APPLICATION_ID,
  PROGRAM_ID,
  PROGRAM_UPDATE_DATE,
  TASKS_RESTRUCTURED_FLAG,
  SUM_EXCEPTION_CODE,
  KCA_OPERATION,
  KCA_SEQ_ID,
  kca_seq_date
)
(
  SELECT
    PROJECT_ACCUM_ID,
    PROJECT_ID,
    TASK_ID,
    ACCUM_PERIOD,
    RESOURCE_ID,
    RESOURCE_LIST_ASSIGNMENT_ID,
    RESOURCE_LIST_ID,
    RESOURCE_LIST_MEMBER_ID,
    LAST_UPDATED_BY,
    LAST_UPDATE_DATE,
    CREATION_DATE,
