    ATTRIBUTE15,
    INCLUDE_NON_NETTABLE,
    SR_RESOURCE_NAME,
    SR_CUSTOMER_ACCT_ID,
    CONDITION_TYPE,
    KCA_OPERATION,
    kca_seq_id,
    kca_seq_date
  FROM bec_raw_dl_ext.MSC_SUB_INVENTORIES
  WHERE
    kca_operation <> 'DELETE'
    AND COALESCE(kca_seq_id, '') <> ''
    AND (COALESCE(PLAN_ID, 0), COALESCE(SR_INSTANCE_ID, 0), COALESCE(ORGANIZATION_ID, 0), COALESCE(SUB_INVENTORY_CODE, 'NA'), KCA_SEQ_ID) IN (
      SELECT
        COALESCE(PLAN_ID, 0) AS PLAN_ID,
        COALESCE(SR_INSTANCE_ID, 0) AS SR_INSTANCE_ID,
        COALESCE(ORGANIZATION_ID, 0) AS ORGANIZATION_ID,
        COALESCE(SUB_INVENTORY_CODE, 'NA') AS SUB_INVENTORY_CODE,
        MAX(KCA_SEQ_ID)
      FROM bec_raw_dl_ext.MSC_SUB_INVENTORIES
      WHERE
        kca_operation <> 'DELETE' AND COALESCE(kca_seq_id, '') <> ''
      GROUP BY
        COALESCE(PLAN_ID, 0),
        COALESCE(SR_INSTANCE_ID, 0),
        COALESCE(ORGANIZATION_ID, 0),
        COALESCE(SUB_INVENTORY_CODE, 'NA')
    )
    AND kca_seq_date > (
      SELECT
        (
          executebegints - prune_days
        )
      FROM bec_etl_ctrl.batch_ods_info
      WHERE
        ods_table_name = 'msc_sub_inventories'
    )
);