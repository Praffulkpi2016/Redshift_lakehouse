  PLAN_ID,
  ORGANIZATION_ID,
  SUB_INVENTORY_CODE,
  NETTING_TYPE,
  SR_INSTANCE_ID,
  DESCRIPTION,
  REFRESH_NUMBER,
  INVENTORY_ATP_CODE,
  LAST_UPDATE_DATE,
  LAST_UPDATED_BY,
  CREATION_DATE,
  CREATED_BY,
  LAST_UPDATE_LOGIN,
  REQUEST_ID,
  PROGRAM_APPLICATION_ID,
  PROGRAM_ID,
