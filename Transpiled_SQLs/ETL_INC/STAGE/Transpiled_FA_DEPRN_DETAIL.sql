        COALESCE(DISTRIBUTION_ID, 0),
        COALESCE(PERIOD_COUNTER, 0)
    )
    AND kca_seq_date > (
      SELECT
        (
          executebegints - prune_days
        )
      FROM bec_etl_ctrl.batch_ods_info
      WHERE
        ods_table_name = 'fa_deprn_detail'
    )
