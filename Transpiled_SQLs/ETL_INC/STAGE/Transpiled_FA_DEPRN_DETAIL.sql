    AND (COALESCE(BOOK_TYPE_CODE, 'NA'), COALESCE(ASSET_ID, 0), COALESCE(DISTRIBUTION_ID, 0), COALESCE(PERIOD_COUNTER, 0), kca_seq_id) IN (
