  CALENDAR_DATE,
  CALENDAR_CODE,
  SEQ_NUM,
  NEXT_SEQ_NUM,
  PRIOR_SEQ_NUM,
  NEXT_DATE,
  PRIOR_DATE,
  CALENDAR_START_DATE,
  CALENDAR_END_DATE,
  DESCRIPTION,
  EXCEPTION_SET_ID,
  SR_INSTANCE_ID,
  REFRESH_NUMBER,
  LAST_UPDATE_DATE,
  LAST_UPDATED_BY,
