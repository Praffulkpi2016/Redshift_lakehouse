)
(
  SELECT
    CALENDAR_DATE,
    CALENDAR_CODE,
    SEQ_NUM,
    NEXT_SEQ_NUM,
    PRIOR_SEQ_NUM,
    NEXT_DATE,
    PRIOR_DATE,
    CALENDAR_START_DATE,
    CALENDAR_END_DATE,
    DESCRIPTION,
    EXCEPTION_SET_ID,
    SR_INSTANCE_ID,
    REFRESH_NUMBER,
    LAST_UPDATE_DATE,
    LAST_UPDATED_BY,
    CREATION_DATE,
    CREATED_BY,
    LAST_UPDATE_LOGIN,
    REQUEST_ID,
    PROGRAM_APPLICATION_ID,
    PROGRAM_ID,
    PROGRAM_UPDATE_DATE,
    ATTRIBUTE_CATEGORY,
    ATTRIBUTE1,
