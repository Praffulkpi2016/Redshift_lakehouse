/* Delete Records */
DELETE FROM silver_bec_ods.XLA_EVENT_CLASSES_TL
WHERE
  (COALESCE(APPLICATION_ID, 0), COALESCE(ENTITY_CODE, 'NA'), COALESCE(EVENT_CLASS_CODE, 'NA'), COALESCE(LANGUAGE, 'NA')) IN (
    SELECT
      COALESCE(stg.APPLICATION_ID, 0) AS APPLICATION_ID,
      COALESCE(stg.ENTITY_CODE, 'NA') AS ENTITY_CODE,
      COALESCE(stg.EVENT_CLASS_CODE, 'NA') AS EVENT_CLASS_CODE,
      COALESCE(stg.LANGUAGE, 'NA') AS LANGUAGE
    FROM silver_bec_ods.XLA_EVENT_CLASSES_TL AS ods, bronze_bec_ods_stg.XLA_EVENT_CLASSES_TL AS stg
    WHERE
      COALESCE(ods.APPLICATION_ID, 0) = COALESCE(stg.APPLICATION_ID, 0)
      AND COALESCE(ods.ENTITY_CODE, 'NA') = COALESCE(stg.ENTITY_CODE, 'NA')
      AND COALESCE(ods.EVENT_CLASS_CODE, 'NA') = COALESCE(stg.EVENT_CLASS_CODE, 'NA')
      AND COALESCE(ods.LANGUAGE, 'NA') = COALESCE(stg.LANGUAGE, 'NA')
      AND stg.kca_operation IN ('INSERT', 'UPDATE')
  );
