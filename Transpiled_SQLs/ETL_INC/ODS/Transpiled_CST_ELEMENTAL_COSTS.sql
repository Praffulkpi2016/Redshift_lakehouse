  LAST_UPDATE_LOGIN,
  STANDARD_COST,
  REQUEST_ID,
  PROGRAM_APPLICATION_ID,
  PROGRAM_ID,
  PROGRAM_UPDATE_DATE,
  KCA_OPERATION,
  IS_DELETED_FLG,
  kca_seq_id,
  kca_seq_date
)
(
  SELECT
    COST_UPDATE_ID,
    ORGANIZATION_ID,
    INVENTORY_ITEM_ID,
    COST_ELEMENT_ID,
    LAST_UPDATE_DATE,
    LAST_UPDATED_BY,
    CREATION_DATE,
    CREATED_BY,
    LAST_UPDATE_LOGIN,
    STANDARD_COST,
    REQUEST_ID,
    PROGRAM_APPLICATION_ID,
    PROGRAM_ID,
    PROGRAM_UPDATE_DATE,
    KCA_OPERATION,
    'N' AS IS_DELETED_FLG,
    CAST(NULLIF(KCA_SEQ_ID, '') AS DECIMAL(36, 0)) AS KCA_SEQ_ID,
    kca_seq_date
  FROM bronze_bec_ods_stg.CST_ELEMENTAL_COSTS
  WHERE
    kca_operation IN ('INSERT', 'UPDATE')
    AND (COST_UPDATE_ID, ORGANIZATION_ID, INVENTORY_ITEM_ID, COST_ELEMENT_ID, kca_seq_id) IN (
      SELECT
        COST_UPDATE_ID,
        ORGANIZATION_ID,
        INVENTORY_ITEM_ID,
        COST_ELEMENT_ID,
        MAX(kca_seq_id)
      FROM bronze_bec_ods_stg.CST_ELEMENTAL_COSTS
      WHERE
        kca_operation IN ('INSERT', 'UPDATE')
      GROUP BY
        COST_UPDATE_ID,
        ORGANIZATION_ID,
        INVENTORY_ITEM_ID,
        COST_ELEMENT_ID
    )
);
