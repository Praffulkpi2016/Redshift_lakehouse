/* Delete Records */
DELETE FROM silver_bec_ods.CST_ELEMENTAL_COSTS
WHERE
  (COST_UPDATE_ID, ORGANIZATION_ID, INVENTORY_ITEM_ID, COST_ELEMENT_ID) IN (
    SELECT
      stg.COST_UPDATE_ID,
      stg.ORGANIZATION_ID,
      stg.INVENTORY_ITEM_ID,
      stg.COST_ELEMENT_ID
    FROM silver_bec_ods.CST_ELEMENTAL_COSTS AS ods, bronze_bec_ods_stg.CST_ELEMENTAL_COSTS AS stg
    WHERE
      ods.COST_UPDATE_ID = stg.COST_UPDATE_ID
      AND ods.ORGANIZATION_ID = stg.ORGANIZATION_ID
      AND ods.INVENTORY_ITEM_ID = stg.INVENTORY_ITEM_ID
      AND ods.COST_ELEMENT_ID = stg.COST_ELEMENT_ID
      AND stg.kca_operation IN ('INSERT', 'UPDATE')
  );
