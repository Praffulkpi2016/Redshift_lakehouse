WHERE
  (
    RESERVATION_ID
  ) IN (
    SELECT
      RESERVATION_ID
    FROM bec_raw_dl_ext.mtl_reservations
    WHERE
      (RESERVATION_ID, KCA_SEQ_ID) IN (
        SELECT
          RESERVATION_ID,
          MAX(KCA_SEQ_ID) AS KCA_SEQ_ID
        FROM bec_raw_dl_ext.mtl_reservations
        GROUP BY
          RESERVATION_ID
      )
      AND kca_operation = 'DELETE'
  );
