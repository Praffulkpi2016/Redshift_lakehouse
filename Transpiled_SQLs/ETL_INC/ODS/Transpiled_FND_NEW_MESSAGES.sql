/* Delete Records */
DELETE FROM silver_bec_ods.fnd_new_messages
WHERE
  (COALESCE(APPLICATION_ID, 0), COALESCE(MESSAGE_NAME, 'NA'), COALESCE(LANGUAGE_CODE, 'NA'), COALESCE(MESSAGE_NUMBER, 0), COALESCE(ZD_EDITION_NAME, 'NA')) IN (
    SELECT
      COALESCE(stg.APPLICATION_ID, 0) AS APPLICATION_ID,
      COALESCE(stg.MESSAGE_NAME, 'NA') AS MESSAGE_NAME,
      COALESCE(stg.LANGUAGE_CODE, 'NA') AS LANGUAGE_CODE,
      COALESCE(stg.MESSAGE_NUMBER, 0) AS MESSAGE_NUMBER,
      COALESCE(stg.ZD_EDITION_NAME, 'NA') AS ZD_EDITION_NAME
    FROM silver_bec_ods.fnd_new_messages AS ods, bronze_bec_ods_stg.fnd_new_messages AS stg
    WHERE
      COALESCE(ods.APPLICATION_ID, 0) = COALESCE(stg.APPLICATION_ID, 0)
      AND COALESCE(ods.MESSAGE_NAME, 'NA') = COALESCE(stg.MESSAGE_NAME, 'NA')
      AND COALESCE(ods.LANGUAGE_CODE, 'NA') = COALESCE(stg.LANGUAGE_CODE, 'NA')
      AND COALESCE(ods.MESSAGE_NUMBER, 0) = COALESCE(stg.MESSAGE_NUMBER, 0)
      AND COALESCE(ods.ZD_EDITION_NAME, 'NA') = COALESCE(stg.ZD_EDITION_NAME, 'NA')
      AND stg.kca_operation IN ('INSERT', 'UPDATE')
  );
