INSERT INTO silver_bec_ods.fnd_new_messages (
  APPLICATION_ID,
  LANGUAGE_CODE,
  MESSAGE_NUMBER,
  MESSAGE_NAME,
  MESSAGE_TEXT,
  CREATION_DATE,
  CREATED_BY,
  LAST_UPDATE_DATE,
  LAST_UPDATED_BY,
  LAST_UPDATE_LOGIN,
  DESCRIPTION,
  TYPE,
  MAX_LENGTH,
  CATEGORY,
  SEVERITY,
  FND_LOG_SEVERITY,
  ZD_EDITION_NAME,
  ZD_SYNC,
  KCA_OPERATION,
  IS_DELETED_FLG,
  kca_seq_id,
  kca_seq_date
)
(
  SELECT
    APPLICATION_ID,
    LANGUAGE_CODE,
    MESSAGE_NUMBER,
    MESSAGE_NAME,
    MESSAGE_TEXT,
    CREATION_DATE,
    CREATED_BY,
    LAST_UPDATE_DATE,
    LAST_UPDATED_BY,
    LAST_UPDATE_LOGIN,
    DESCRIPTION,
    TYPE,
    MAX_LENGTH,
    CATEGORY,
    SEVERITY,
    FND_LOG_SEVERITY,
    ZD_EDITION_NAME,
    ZD_SYNC,
    KCA_OPERATION,
    'N' AS IS_DELETED_FLG,
    CAST(NULLIF(KCA_SEQ_ID, '') AS DECIMAL(36, 0)) AS KCA_SEQ_ID,
    kca_seq_date
  FROM bronze_bec_ods_stg.fnd_new_messages
  WHERE
    kca_operation IN ('INSERT', 'UPDATE')
    AND (COALESCE(APPLICATION_ID, 0), COALESCE(MESSAGE_NAME, 'NA'), COALESCE(LANGUAGE_CODE, 'NA'), COALESCE(MESSAGE_NUMBER, 0), COALESCE(ZD_EDITION_NAME, 'NA'), kca_seq_id) IN (
      SELECT
        COALESCE(APPLICATION_ID, 0),
        COALESCE(MESSAGE_NAME, 'NA'),
        COALESCE(LANGUAGE_CODE, 'NA'),
        COALESCE(MESSAGE_NUMBER, 0),
        COALESCE(ZD_EDITION_NAME, 'NA') AS ZD_EDITION_NAME,
        MAX(kca_seq_id)
      FROM bronze_bec_ods_stg.fnd_new_messages
      WHERE
        kca_operation IN ('INSERT', 'UPDATE')
      GROUP BY
        COALESCE(APPLICATION_ID, 0),
        COALESCE(MESSAGE_NAME, 'NA'),
        COALESCE(LANGUAGE_CODE, 'NA'),
        COALESCE(MESSAGE_NUMBER, 0),
        COALESCE(ZD_EDITION_NAME, 'NA')
    )
);
