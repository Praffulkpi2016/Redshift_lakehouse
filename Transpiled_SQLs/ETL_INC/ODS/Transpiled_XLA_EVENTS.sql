/* Delete Records */
DELETE FROM silver_bec_ods.XLA_EVENTS
WHERE
  (EVENT_ID, APPLICATION_ID) IN (
    SELECT
      stg.EVENT_ID,
      stg.APPLICATION_ID
    FROM silver_bec_ods.XLA_EVENTS AS ods, bronze_bec_ods_stg.XLA_EVENTS AS stg
    WHERE
      ods.EVENT_ID = stg.EVENT_ID
      AND ods.APPLICATION_ID = stg.APPLICATION_ID
      AND stg.kca_operation IN ('INSERT', 'UPDATE')
  );
