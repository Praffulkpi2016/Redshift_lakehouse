UPDATE silver_bec_ods.XLA_EVENTS SET IS_DELETED_FLG = 'Y'
WHERE
  (EVENT_ID, APPLICATION_ID) IN (
    SELECT
      ext.EVENT_ID,
      ext.APPLICATION_ID
    FROM silver_bec_ods.XLA_EVENTS AS ods, bec_raw_dl_ext.XLA_EVENTS AS ext
    WHERE
      ods.EVENT_ID = ext.EVENT_ID
      AND ods.APPLICATION_ID = ext.APPLICATION_ID
