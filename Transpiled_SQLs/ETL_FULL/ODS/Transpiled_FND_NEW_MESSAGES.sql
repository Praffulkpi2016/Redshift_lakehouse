DROP TABLE IF EXISTS silver_bec_ods.fnd_new_messages;
CREATE TABLE IF NOT EXISTS silver_bec_ods.fnd_new_messages (
  APPLICATION_ID DECIMAL(15, 0),
  LANGUAGE_CODE STRING,
  MESSAGE_NUMBER DECIMAL(9, 0),
  MESSAGE_NAME STRING,
  MESSAGE_TEXT STRING,
  CREATION_DATE TIMESTAMP,
  CREATED_BY DECIMAL(15, 0),
  LAST_UPDATE_DATE TIMESTAMP,
  LAST_UPDATED_BY DECIMAL(15, 0),
  LAST_UPDATE_LOGIN DECIMAL(15, 0),
  DESCRIPTION STRING,
  TYPE STRING,
  MAX_LENGTH DECIMAL(28, 10),
  CATEGORY STRING,
  SEVERITY STRING,
  FND_LOG_SEVERITY DECIMAL(28, 10),
  ZD_EDITION_NAME STRING,
  ZD_SYNC STRING,
  KCA_OPERATION STRING,
  IS_DELETED_FLG STRING,
  kca_seq_id DECIMAL(36, 0),
  kca_seq_date TIMESTAMP
);
