DROP TABLE IF EXISTS silver_bec_ods.fa_distribution_history;
CREATE TABLE IF NOT EXISTS silver_bec_ods.fa_distribution_history (
  DISTRIBUTION_ID DECIMAL(15, 0),
  BOOK_TYPE_CODE STRING,
  ASSET_ID DECIMAL(15, 0),
  UNITS_ASSIGNED DECIMAL(28, 10),
  DATE_EFFECTIVE TIMESTAMP,
  CODE_COMBINATION_ID DECIMAL(15, 0),
  LOCATION_ID DECIMAL(15, 0),
  TRANSACTION_HEADER_ID_IN DECIMAL(15, 0),
  LAST_UPDATE_DATE TIMESTAMP,
  LAST_UPDATED_BY DECIMAL(15, 0),
  DATE_INEFFECTIVE TIMESTAMP,
  ASSIGNED_TO DECIMAL(15, 0),
  TRANSACTION_HEADER_ID_OUT DECIMAL(15, 0),
  TRANSACTION_UNITS DECIMAL(28, 10),
  RETIREMENT_ID DECIMAL(15, 0),
  LAST_UPDATE_LOGIN DECIMAL(15, 0),
  CAPITAL_ADJ_ACCOUNT_CCID DECIMAL(15, 0),
  GENERAL_FUND_ACCOUNT_CCID DECIMAL(15, 0),
  KCA_OPERATION STRING,
  IS_DELETED_FLG STRING,
  kca_seq_id DECIMAL(36, 0),
  kca_seq_date TIMESTAMP
);
