DROP TABLE IF EXISTS silver_bec_ods.JTF_TASK_PRIORITIES_TL;
CREATE TABLE IF NOT EXISTS silver_bec_ods.JTF_TASK_PRIORITIES_TL (
  TASK_PRIORITY_ID DECIMAL(15, 0),
  LANGUAGE STRING,
  SOURCE_LANG STRING,
  NAME STRING,
  DESCRIPTION STRING,
  CREATED_BY DECIMAL(15, 0),
  CREATION_DATE TIMESTAMP,
  LAST_UPDATED_BY DECIMAL(15, 0),
  LAST_UPDATE_DATE TIMESTAMP,
  LAST_UPDATE_LOGIN DECIMAL(15, 0),
  SECURITY_GROUP_ID DECIMAL(15, 0),
  ZD_EDITION_NAME STRING,
  ZD_SYNC STRING,
  KCA_OPERATION STRING,
  IS_DELETED_FLG STRING,
  kca_seq_id DECIMAL(36, 0),
  kca_seq_date TIMESTAMP
);
INSERT INTO silver_bec_ods.JTF_TASK_PRIORITIES_TL (
  TASK_PRIORITY_ID,
  LANGUAGE,
  SOURCE_LANG,
