DROP table IF EXISTS silver_bec_ods.FND_USER;
CREATE TABLE IF NOT EXISTS silver_bec_ods.FND_USER (
  USER_ID DECIMAL(15, 0),
  USER_NAME STRING,
  LAST_UPDATE_DATE TIMESTAMP,
  LAST_UPDATED_BY DECIMAL(15, 0),
  CREATION_DATE TIMESTAMP,
  CREATED_BY DECIMAL(15, 0), /* ,LAST_UPDATE_LOGIN	NUMERIC(15,0)   */
  ENCRYPTED_FOUNDATION_PASSWORD STRING,
  ENCRYPTED_USER_PASSWORD STRING, /* ,SESSION_NUMBER	 NUMERIC(28,10)   */
  START_DATE TIMESTAMP,
  END_DATE TIMESTAMP,
  DESCRIPTION STRING, /* ,LAST_LOGON_DATE TIMESTAMP WITHOUT TIME ZONE   */
  PASSWORD_DATE TIMESTAMP,
  PASSWORD_ACCESSES_LEFT DECIMAL(15, 0),
  PASSWORD_LIFESPAN_ACCESSES DECIMAL(15, 0),
  PASSWORD_LIFESPAN_DAYS DECIMAL(15, 0),
  EMPLOYEE_ID DECIMAL(15, 0),
  EMAIL_ADDRESS STRING,
  FAX STRING,
  CUSTOMER_ID DECIMAL(15, 0),
  SUPPLIER_ID DECIMAL(15, 0),
  WEB_PASSWORD STRING, /* ,SECURITY_GROUP_ID	NUMERIC(28,10)   */
  GCN_CODE_COMBINATION_ID DECIMAL(15, 0),
  PERSON_PARTY_ID DECIMAL(28, 10),
  KCA_OPERATION STRING,
  IS_DELETED_FLG STRING,
  kca_seq_id DECIMAL(36, 0),
  kca_seq_date TIMESTAMP
);
