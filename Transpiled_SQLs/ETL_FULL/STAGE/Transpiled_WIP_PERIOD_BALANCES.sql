DROP table IF EXISTS bronze_bec_ods_stg.WIP_PERIOD_BALANCES;
CREATE TABLE bronze_bec_ods_stg.WIP_PERIOD_BALANCES AS
SELECT
  *
FROM bec_raw_dl_ext.WIP_PERIOD_BALANCES
WHERE
  kca_operation <> 'DELETE'
  AND (COALESCE(WIP_ENTITY_ID, 0), COALESCE(REPETITIVE_SCHEDULE_ID, 0), COALESCE(ACCT_PERIOD_ID, 0), last_update_date) IN (
    SELECT
      COALESCE(WIP_ENTITY_ID, 0) AS WIP_ENTITY_ID,
      COALESCE(REPETITIVE_SCHEDULE_ID, 0) AS REPETITIVE_SCHEDULE_ID,
      COALESCE(ACCT_PERIOD_ID, 0) AS ACCT_PERIOD_ID,
      MAX(last_update_date)
    FROM bec_raw_dl_ext.WIP_PERIOD_BALANCES
    WHERE
      kca_operation <> 'DELETE' AND COALESCE(kca_seq_id, '') = ''
    GROUP BY
      WIP_ENTITY_ID,
      REPETITIVE_SCHEDULE_ID,
      ACCT_PERIOD_ID
  );