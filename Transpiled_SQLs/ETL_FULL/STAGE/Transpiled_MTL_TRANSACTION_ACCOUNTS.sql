DROP TABLE IF EXISTS bronze_bec_ods_stg.MTL_TRANSACTION_ACCOUNTS;
CREATE TABLE bronze_bec_ods_stg.MTL_TRANSACTION_ACCOUNTS AS
SELECT
  *
FROM bec_raw_dl_ext.MTL_TRANSACTION_ACCOUNTS
WHERE
  kca_operation <> 'DELETE'
  AND (COALESCE(TRANSACTION_ID, 0), COALESCE(REFERENCE_ACCOUNT, 0), COALESCE(COST_ELEMENT_ID, 0), COALESCE(PRIMARY_QUANTITY, 0), COALESCE(RESOURCE_ID, 0), COALESCE(ACCOUNTING_LINE_TYPE, 0), COALESCE(BASE_TRANSACTION_VALUE, 0), last_update_date) IN (
    SELECT
      COALESCE(TRANSACTION_ID, 0),
      COALESCE(REFERENCE_ACCOUNT, 0),
      COALESCE(COST_ELEMENT_ID, 0),
      COALESCE(PRIMARY_QUANTITY, 0),
      COALESCE(RESOURCE_ID, 0),
      COALESCE(ACCOUNTING_LINE_TYPE, 0),
      COALESCE(BASE_TRANSACTION_VALUE, 0),
      MAX(last_update_date)
    FROM bec_raw_dl_ext.MTL_TRANSACTION_ACCOUNTS
    WHERE
      kca_operation <> 'DELETE' /* and nvl(kca_seq_id,'') = '' */
    GROUP BY
      COALESCE(TRANSACTION_ID, 0),
      COALESCE(REFERENCE_ACCOUNT, 0),
      COALESCE(COST_ELEMENT_ID, 0),
      COALESCE(PRIMARY_QUANTITY, 0),
      COALESCE(RESOURCE_ID, 0),
      COALESCE(ACCOUNTING_LINE_TYPE, 0),
      COALESCE(BASE_TRANSACTION_VALUE, 0)
  );