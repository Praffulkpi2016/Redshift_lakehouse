begin;

DROP TABLE if exists bec_ods.WIP_TRANSACTION_ACCOUNTS;

CREATE TABLE IF NOT EXISTS bec_ods.WIP_TRANSACTION_ACCOUNTS
(
	 TRANSACTION_ID NUMERIC(15,0)   ENCODE az64 
	,REFERENCE_ACCOUNT NUMERIC(15,0)   ENCODE az64 
	,LAST_UPDATE_DATE TIMESTAMP WITHOUT TIME ZONE   ENCODE az64 
	,LAST_UPDATED_BY NUMERIC(15,0)   ENCODE az64
	,CREATION_DATE TIMESTAMP WITHOUT TIME ZONE   ENCODE az64 
	,CREATED_BY NUMERIC(15,0)   ENCODE az64 
	,LAST_UPDATE_LOGIN NUMERIC(15,0)   ENCODE az64  
	,ORGANIZATION_ID NUMERIC(15,0)   ENCODE az64
	,TRANSACTION_DATE TIMESTAMP WITHOUT TIME ZONE   ENCODE az64 
	,WIP_ENTITY_ID NUMERIC(15,0)   ENCODE az64
	,REPETITIVE_SCHEDULE_ID NUMERIC(15,0)   ENCODE az64 
	,ACCOUNTING_LINE_TYPE NUMERIC(15,0)   ENCODE az64  
	,TRANSACTION_VALUE NUMERIC(28,10)   ENCODE az64 
	,BASE_TRANSACTION_VALUE NUMERIC(28,10)   ENCODE az64  
	,CONTRA_SET_ID NUMERIC(15,0)   ENCODE az64 
	,PRIMARY_QUANTITY NUMERIC(28,10)   ENCODE az64  
	,RATE_OR_AMOUNT NUMERIC(28,10)   ENCODE az64  
	,BASIS_TYPE NUMERIC(15,0)   ENCODE az64 
	,RESOURCE_ID NUMERIC(15,0)   ENCODE az64 
	,COST_ELEMENT_ID NUMERIC(15,0)   ENCODE az64 
	,ACTIVITY_ID NUMERIC(15,0)   ENCODE az64 
	,CURRENCY_CODE VARCHAR(15)	ENCODE lzo
	,CURRENCY_CONVERSION_DATE TIMESTAMP WITHOUT TIME ZONE   ENCODE az64 
	,CURRENCY_CONVERSION_TYPE VARCHAR(10)	ENCODE lzo
	,CURRENCY_CONVERSION_RATE NUMERIC(28,10)   ENCODE az64 
	,OVERHEAD_BASIS_FACTOR NUMERIC(28,10)   ENCODE az64  
	,BASIS_RESOURCE_ID NUMERIC(15,0)   ENCODE az64 
	,GL_BATCH_ID NUMERIC(15,0)   ENCODE az64 
	,REQUEST_ID NUMERIC(15,0)   ENCODE az64 
	,PROGRAM_APPLICATION_ID NUMERIC(15,0)   ENCODE az64 
	,PROGRAM_ID NUMERIC(15,0)   ENCODE az64 
	,PROGRAM_UPDATE_DATE TIMESTAMP WITHOUT TIME ZONE   ENCODE az64 
	,GL_SL_LINK_ID NUMERIC(15,0)   ENCODE az64 
	,WIP_SUB_LEDGER_ID NUMERIC(15,0)   ENCODE az64 
	,ENCUMBRANCE_TYPE_ID NUMERIC(15,0)   ENCODE az64
	,KCA_OPERATION VARCHAR(10)   ENCODE lzo
    ,IS_DELETED_FLG VARCHAR(2) ENCODE lzo
	,kca_seq_id NUMERIC(36,0)   ENCODE az64
	,kca_seq_date TIMESTAMP WITHOUT TIME ZONE   ENCODE az64
)
DISTSTYLE
auto;

INSERT INTO bec_ods.WIP_TRANSACTION_ACCOUNTS (
    TRANSACTION_ID, 
	REFERENCE_ACCOUNT, 
	LAST_UPDATE_DATE, 
	LAST_UPDATED_BY, 
	CREATION_DATE, 
	CREATED_BY, 
	LAST_UPDATE_LOGIN, 
	ORGANIZATION_ID, 
	TRANSACTION_DATE, 
	WIP_ENTITY_ID, 
	REPETITIVE_SCHEDULE_ID, 
	ACCOUNTING_LINE_TYPE, 
	TRANSACTION_VALUE, 
	BASE_TRANSACTION_VALUE, 
	CONTRA_SET_ID, 
	PRIMARY_QUANTITY, 
	RATE_OR_AMOUNT, 
	BASIS_TYPE, 
	RESOURCE_ID, 
	COST_ELEMENT_ID, 
	ACTIVITY_ID, 
	CURRENCY_CODE, 
	CURRENCY_CONVERSION_DATE, 
	CURRENCY_CONVERSION_TYPE, 
	CURRENCY_CONVERSION_RATE, 
	OVERHEAD_BASIS_FACTOR, 
	BASIS_RESOURCE_ID, 
	GL_BATCH_ID, 
	REQUEST_ID, 
	PROGRAM_APPLICATION_ID, 
	PROGRAM_ID, 
	PROGRAM_UPDATE_DATE, 
	GL_SL_LINK_ID, 
	WIP_SUB_LEDGER_ID, 
	ENCUMBRANCE_TYPE_ID,
	KCA_OPERATION,
	IS_DELETED_FLG,
	kca_seq_id
	,kca_seq_date
)
    SELECT
        TRANSACTION_ID, 
		REFERENCE_ACCOUNT, 
		LAST_UPDATE_DATE, 
		LAST_UPDATED_BY, 
		CREATION_DATE, 
		CREATED_BY, 
		LAST_UPDATE_LOGIN, 
		ORGANIZATION_ID, 
		TRANSACTION_DATE, 
		WIP_ENTITY_ID, 
		REPETITIVE_SCHEDULE_ID, 
		ACCOUNTING_LINE_TYPE, 
		TRANSACTION_VALUE, 
		BASE_TRANSACTION_VALUE, 
		CONTRA_SET_ID, 
		PRIMARY_QUANTITY, 
		RATE_OR_AMOUNT, 
		BASIS_TYPE, 
		RESOURCE_ID, 
		COST_ELEMENT_ID, 
		ACTIVITY_ID, 
		CURRENCY_CODE, 
		CURRENCY_CONVERSION_DATE, 
		CURRENCY_CONVERSION_TYPE, 
		CURRENCY_CONVERSION_RATE, 
		OVERHEAD_BASIS_FACTOR, 
		BASIS_RESOURCE_ID, 
		GL_BATCH_ID, 
		REQUEST_ID, 
		PROGRAM_APPLICATION_ID, 
		PROGRAM_ID, 
		PROGRAM_UPDATE_DATE, 
		GL_SL_LINK_ID, 
		WIP_SUB_LEDGER_ID, 
		ENCUMBRANCE_TYPE_ID,
		KCA_OPERATION,
		'N' as IS_DELETED_FLG,
		cast(NULLIF(KCA_SEQ_ID,'') as numeric(36,0)) as KCA_SEQ_ID
		,kca_seq_date
    FROM
        bec_ods_stg.WIP_TRANSACTION_ACCOUNTS;

end;		

UPDATE bec_etl_ctrl.batch_ods_info
SET
    load_type = 'I',
    last_refresh_date = getdate()
WHERE
    ods_table_name = 'wip_transaction_accounts';
	
COMMIT;