begin;

drop table if exists bec_ods.FND_FLEX_VALUE_SETS;

CREATE TABLE IF NOT EXISTS bec_ods.FND_FLEX_VALUE_SETS
(
FLEX_VALUE_SET_ID	NUMERIC(10,0)   ENCODE az64
,FLEX_VALUE_SET_NAME	VARCHAR(60)   ENCODE lzo
,LAST_UPDATE_DATE TIMESTAMP WITHOUT TIME ZONE   ENCODE az64
,LAST_UPDATED_BY	NUMERIC(15,0)   ENCODE az64
,CREATION_DATE	 TIMESTAMP WITHOUT TIME ZONE   ENCODE az64
,CREATED_BY	NUMERIC(15,0)   ENCODE az64
,LAST_UPDATE_LOGIN	NUMERIC(15,0)   ENCODE az64
,VALIDATION_TYPE	VARCHAR(1)   ENCODE lzo
,PROTECTED_FLAG	VARCHAR(1)   ENCODE lzo
,SECURITY_ENABLED_FLAG	VARCHAR(1)   ENCODE lzo
,LONGLIST_FLAG	VARCHAR(1)   ENCODE lzo
,FORMAT_TYPE	VARCHAR(1)   ENCODE lzo
,MAXIMUM_SIZE	NUMERIC(3,0)   ENCODE az64
,ALPHANUMERIC_ALLOWED_FLAG	VARCHAR(1)   ENCODE lzo
,UPPERCASE_ONLY_FLAG	VARCHAR(1)   ENCODE lzo
,NUMERIC_MODE_ENABLED_FLAG	VARCHAR(1)   ENCODE lzo
,DESCRIPTION	VARCHAR(240)   ENCODE lzo
,DEPENDANT_DEFAULT_VALUE	VARCHAR(60)   ENCODE lzo
,DEPENDANT_DEFAULT_MEANING	VARCHAR(240)   ENCODE lzo
,PARENT_FLEX_VALUE_SET_ID	NUMERIC(10,0)   ENCODE az64
,MINIMUM_VALUE	VARCHAR(150)   ENCODE lzo
,MAXIMUM_VALUE	VARCHAR(150)   ENCODE lzo
,NUMBER_PRECISION	NUMERIC(2,0)   ENCODE az64
--,SECURITY_GROUP_ID	NUMERIC(28,10)   ENCODE az64
,ZD_EDITION_NAME  VARCHAR(30)   ENCODE lzo
,ZD_SYNC VARCHAR(30)   ENCODE lzo
,KCA_OPERATION VARCHAR(10)   ENCODE lzo
,IS_DELETED_FLG VARCHAR(2) ENCODE lzo
,kca_seq_id NUMERIC(36,0)   ENCODE az64
	,kca_seq_date TIMESTAMP WITHOUT TIME ZONE ENCODE az64
)
DISTSTYLE
auto;

INSERT INTO bec_ods.FND_FLEX_VALUE_SETS 
(
FLEX_VALUE_SET_ID
,FLEX_VALUE_SET_NAME
,LAST_UPDATE_DATE
,LAST_UPDATED_BY
,CREATION_DATE
,CREATED_BY
,LAST_UPDATE_LOGIN
,VALIDATION_TYPE
,PROTECTED_FLAG
,SECURITY_ENABLED_FLAG
,LONGLIST_FLAG
,FORMAT_TYPE
,MAXIMUM_SIZE
,ALPHANUMERIC_ALLOWED_FLAG
,UPPERCASE_ONLY_FLAG
,NUMERIC_MODE_ENABLED_FLAG
,DESCRIPTION
,DEPENDANT_DEFAULT_VALUE
,DEPENDANT_DEFAULT_MEANING
,PARENT_FLEX_VALUE_SET_ID
,MINIMUM_VALUE
,MAXIMUM_VALUE
,NUMBER_PRECISION
,ZD_EDITION_NAME
,ZD_SYNC
,KCA_OPERATION
,IS_DELETED_FLG
,kca_seq_id,
	kca_seq_date
)
( SELECT 
FLEX_VALUE_SET_ID
,FLEX_VALUE_SET_NAME
,LAST_UPDATE_DATE
,LAST_UPDATED_BY
,CREATION_DATE
,CREATED_BY
,LAST_UPDATE_LOGIN
,VALIDATION_TYPE
,PROTECTED_FLAG
,SECURITY_ENABLED_FLAG
,LONGLIST_FLAG
,FORMAT_TYPE
,MAXIMUM_SIZE
,ALPHANUMERIC_ALLOWED_FLAG
,UPPERCASE_ONLY_FLAG
,NUMERIC_MODE_ENABLED_FLAG
,DESCRIPTION
,DEPENDANT_DEFAULT_VALUE
,DEPENDANT_DEFAULT_MEANING
,PARENT_FLEX_VALUE_SET_ID
,MINIMUM_VALUE
,MAXIMUM_VALUE
,NUMBER_PRECISION
,ZD_EDITION_NAME
,ZD_SYNC
,KCA_OPERATION
,'N' as IS_DELETED_FLG
,cast(NULLIF(KCA_SEQ_ID,'') as numeric(36,0)) as KCA_SEQ_ID,
	kca_seq_date
FROM
        bec_ods_stg.fnd_flex_value_sets);

end;

UPDATE bec_etl_ctrl.batch_ods_info
SET
    load_type = 'I',
    last_refresh_date = getdate()
WHERE
    ods_table_name = 'fnd_flex_value_sets';
	
commit;