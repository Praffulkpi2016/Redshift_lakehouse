	LAST_UPDATED_BY,
	LAST_UPDATE_DATE,
	LAST_UPDATE_LOGIN,
	CREATED_BY,
	CREATION_DATE,
	REQUEST_ID,
	PROGRAM_APPLICATION_ID,
	PROGRAM_ID,
	PROGRAM_UPDATE_DATE,
	REQ_HEADER_ID,
	REQ_LINE_ID,
	PO_HEADER_ID,
	PO_RELEASE_ID,
	PO_LINE_ID,
	PO_LINE_LOCATION_ID,
	PO_DISTRIBUTION_ID,
	SHIPMENT_HEADER_ID,
	SHIPMENT_LINE_ID,
	RCV_TRANSACTION_ID,
	ITEM_ID,
	ITEM_REVISION,
	CATEGORY_ID,
	QUANTITY,
	UNIT_OF_MEASURE,
	TO_ORG_PRIMARY_QUANTITY,
	TO_ORG_PRIMARY_UOM,
