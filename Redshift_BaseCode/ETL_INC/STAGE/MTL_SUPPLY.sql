	MRP_PRIMARY_QUANTITY,
	MRP_PRIMARY_UOM,
	MRP_EXPECTED_DELIVERY_DATE,
	MRP_DESTINATION_TYPE_CODE,
	MRP_TO_ORGANIZATION_ID,
	MRP_TO_SUBINVENTORY,
	CHANGE_FLAG,
	CHANGE_TYPE,
	COST_GROUP_ID,
	EXCLUDE_FROM_PLANNING,
    KCA_OPERATION,
	kca_seq_id,
	kca_seq_date)
(
	select
		SUPPLY_TYPE_CODE,
		SUPPLY_SOURCE_ID,
		LAST_UPDATED_BY,
		LAST_UPDATE_DATE,
		LAST_UPDATE_LOGIN,
		CREATED_BY,
		CREATION_DATE,
		REQUEST_ID,
		PROGRAM_APPLICATION_ID,
		PROGRAM_ID,
		PROGRAM_UPDATE_DATE,
		REQ_HEADER_ID,
		REQ_LINE_ID,
		PO_HEADER_ID,
		PO_RELEASE_ID,
		PO_LINE_ID,
		PO_LINE_LOCATION_ID,
		PO_DISTRIBUTION_ID,
