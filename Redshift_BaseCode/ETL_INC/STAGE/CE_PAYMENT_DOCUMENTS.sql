	ATTRIBUTE10,
	ATTRIBUTE11,
	ATTRIBUTE12,
	ATTRIBUTE13,
	ATTRIBUTE14,
	ATTRIBUTE15,
	INACTIVE_DATE,
	CREATED_BY,
	CREATION_DATE,
	LAST_UPDATED_BY,
	LAST_UPDATE_DATE,
	LAST_UPDATE_LOGIN,
	OBJECT_VERSION_NUMBER
	,KCA_OPERATION
	,kca_seq_id
	,kca_seq_date)
SELECT
	PAYMENT_DOCUMENT_ID,
	PAYMENT_DOC_CATEGORY,
	PAYMENT_DOCUMENT_NAME,
	PAYMENT_INSTRUCTION_ID,
	INTERNAL_BANK_ACCOUNT_ID,
	PAPER_STOCK_TYPE,
	ATTACHED_REMITTANCE_STUB_FLAG,
