begin;

-- Delete Records

delete from bec_ods.IBY_PMT_INSTR_USES_ALL
where NVL(INSTRUMENT_PAYMENT_USE_ID,0) in (
select NVL(stg.INSTRUMENT_PAYMENT_USE_ID,0) 
from bec_ods.IBY_PMT_INSTR_USES_ALL ods, bec_ods_stg.IBY_PMT_INSTR_USES_ALL stg
where ods.INSTRUMENT_PAYMENT_USE_ID = stg.INSTRUMENT_PAYMENT_USE_ID
and stg.kca_operation IN ('INSERT','UPDATE')
);

commit;

-- Insert records

insert into bec_ods.IBY_PMT_INSTR_USES_ALL
(		INSTRUMENT_PAYMENT_USE_ID,
	PAYMENT_FLOW,
	EXT_PMT_PARTY_ID,
	INSTRUMENT_TYPE,
	INSTRUMENT_ID,
	PAYMENT_FUNCTION,
	ORDER_OF_PREFERENCE,
	CREATED_BY,
	CREATION_DATE,
	LAST_UPDATED_BY,
	LAST_UPDATE_DATE,
	LAST_UPDATE_LOGIN,
	OBJECT_VERSION_NUMBER,
	START_DATE,
	END_DATE,
	DEBIT_AUTH_FLAG,
	DEBIT_AUTH_METHOD,
	DEBIT_AUTH_REFERENCE,
	DEBIT_AUTH_BEGIN,
	DEBIT_AUTH_END,
	ATTRIBUTE_CATEGORY,
	ATTRIBUTE1,
	ATTRIBUTE2,
	ATTRIBUTE3,
	ATTRIBUTE4,
	ATTRIBUTE5,
	ATTRIBUTE6,
	ATTRIBUTE7,
	ATTRIBUTE8,
	ATTRIBUTE9,
