	ATTRIBUTE13,
	ATTRIBUTE14,
	ATTRIBUTE15
,KCA_OPERATION
,IS_DELETED_FLG
,KCA_SEQ_ID,
	kca_seq_date)
(select
	INSTRUMENT_PAYMENT_USE_ID,
	PAYMENT_FLOW,
	EXT_PMT_PARTY_ID,
	INSTRUMENT_TYPE,
	INSTRUMENT_ID,
	PAYMENT_FUNCTION,
	ORDER_OF_PREFERENCE,
	CREATED_BY,
	CREATION_DATE,
	LAST_UPDATED_BY,
	LAST_UPDATE_DATE,
	LAST_UPDATE_LOGIN,
	OBJECT_VERSION_NUMBER,
	START_DATE,
	END_DATE,
	DEBIT_AUTH_FLAG,
	DEBIT_AUTH_METHOD,
	DEBIT_AUTH_REFERENCE,
	DEBIT_AUTH_BEGIN,
	DEBIT_AUTH_END,
	ATTRIBUTE_CATEGORY,
	ATTRIBUTE1,
	ATTRIBUTE2,
