where (PAYMENT_DOCUMENT_ID )  in
(
select PAYMENT_DOCUMENT_ID  from bec_raw_dl_ext.CE_PAYMENT_DOCUMENTS
where (PAYMENT_DOCUMENT_ID ,KCA_SEQ_ID)
in 
(
select PAYMENT_DOCUMENT_ID ,max(KCA_SEQ_ID) as KCA_SEQ_ID 
from bec_raw_dl_ext.CE_PAYMENT_DOCUMENTS
group by PAYMENT_DOCUMENT_ID 
) 
and kca_operation= 'DELETE'
);
